					_this.ruleForm.zhengshuwenjian = 'upload/' + res.file;
					_this.$forceUpdate();
					_this.$nextTick(()=>{
						_this.styleChange()
					})
				});
			},

			getUUID () {
				return new Date().getTime();
			},
			async onSubmitTap() {























//跨表计算判断
				var obj;
				if((!this.ruleForm.zhengshumingcheng)){
					this.$utils.msg(`证书名称不能为空`);
					return
				}
				if((!this.ruleForm.zhengshuwenjian)){
