	import wPicker from "@/components/w-picker/w-picker.vue";
    import xiaEditor from '@/components/xia-editor/xia-editor';
	export default {
		data() {
			return {
				cross:'',
				ruleForm: {
				shequzhanghao: '',
				shequmingcheng: '',
				huodongmingcheng: '',
				huodongtupian: '',
				zhengshumingcheng: '',
				zhengshuwenjian: '',
				fafangshijian: '',
				zhiyuanzhezhanghao: '',
				zhiyuanzhexingming: '',
				beizhu: '',
				userid: '',
				},
				zhiyuanzhezhanghaoOptions: [],
				zhiyuanzhezhanghaoIndex: 0,
				// 登录用户信息
				user: {},
                ro:{
                   shequzhanghao : false,
                   shequmingcheng : false,
                   huodongmingcheng : false,
                   huodongtupian : false,
                   zhengshumingcheng : false,
                   zhengshuwenjian : false,
                   fafangshijian : false,
                   zhiyuanzhezhanghao : false,
                   zhiyuanzhexingming : false,
                   beizhu : false,
                   userid : false,
                },
			}
		},
		components: {
			wPicker,
            xiaEditor
		},
		computed: {
			baseUrl() {
				return this.$base.url;
			},



		},
		async onLoad(options) {
            this.ruleForm.fafangshijian = this.$utils.getCurDateTime();

			let table = uni.getStorageSync("nowTable");
			// 获取用户信息
			let res = await this.$api.session(table);
			this.user = res.data;
			
			// ss读取
			this.ruleForm.shequzhanghao = this.user.shequzhanghao
			this.ro.shequzhanghao = true;
			this.ruleForm.shequmingcheng = this.user.shequmingcheng
			this.ro.shequmingcheng = true;


			// 下2
			res = await this.$api.option(`zhiyuanzhe`,`zhiyuanzhezhanghao`,{});
			this.zhiyuanzhezhanghaoOptions = res.data;

			// 如果有登录，获取登录后保存的userid
			this.ruleForm.userid = uni.getStorageSync("userid")
			if (options.refid) {
				// 如果上一级页面传递了refid，获取改refid数据信息
				this.ruleForm.refid = options.refid;
				this.ruleForm.nickname = uni.getStorageSync("nickname");
			}
			// 如果是更新操作
			if (options.id) {
				this.ruleForm.id = options.id;
				// 获取信息
				res = await this.$api.info(`zhengshuxinxi`, this.ruleForm.id);
				this.ruleForm = res.data;
			}
			// 跨表
			this.cross = options.cross;
			if(options.cross){
				var obj = uni.getStorageSync('crossObj');
				for (var o in obj){
					if(o=='shequzhanghao'){
					this.ruleForm.shequzhanghao = obj[o];
					this.ro.shequzhanghao = true;
					continue;
					}
					if(o=='shequmingcheng'){
					this.ruleForm.shequmingcheng = obj[o];
					this.ro.shequmingcheng = true;
					continue;
					}
					if(o=='huodongmingcheng'){
					this.ruleForm.huodongmingcheng = obj[o];
					this.ro.huodongmingcheng = true;
					continue;
					}
					if(o=='huodongtupian'){
					this.ruleForm.huodongtupian = obj[o].split(",")[0];
					this.ro.huodongtupian = true;
					continue;
					}
					if(o=='zhengshumingcheng'){
					this.ruleForm.zhengshumingcheng = obj[o];
					this.ro.zhengshumingcheng = true;
					continue;
					}
					if(o=='zhengshuwenjian'){
					this.ruleForm.zhengshuwenjian = obj[o];
					this.ro.zhengshuwenjian = true;
					continue;
					}
					if(o=='fafangshijian'){
					this.ruleForm.fafangshijian = obj[o];
					this.ro.fafangshijian = true;
					continue;
					}
					if(o=='zhiyuanzhezhanghao'){
					this.ruleForm.zhiyuanzhezhanghao = obj[o];
					this.ro.zhiyuanzhezhanghao = true;
					continue;
					}
					if(o=='zhiyuanzhexingming'){
					this.ruleForm.zhiyuanzhexingming = obj[o];
					this.ro.zhiyuanzhexingming = true;
					continue;
					}
					if(o=='beizhu'){
					this.ruleForm.beizhu = obj[o];
					this.ro.beizhu = true;
					continue;
					}
					if(o=='userid'){
					this.ruleForm.userid = obj[o];
					this.ro.userid = true;
					continue;
					}
				}
			}
